
public class Tyres {
    private String name;
    private String size;

    public Tyres(String name, String size) {
        this.name = name;
        this.size = size;
    }

    public Tyres() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
}
